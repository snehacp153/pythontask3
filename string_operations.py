str = input("enter a string:")
# length 
print("length of a string is:",len(str))
# capitalize
print("capitalize the string :", str.capitalize())
# uppercase
print("capitalize each word in a string:",str.upper())
# lowercase
print("capitalize each word in a string into lowercase:",str.lower())
# slicing
print("slice the string 2 to 6:",str[2:6])
# negative slicing
print("negative slicing:",str[:-1])